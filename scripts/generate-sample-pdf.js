// scripts/generate-sample-pdf.js

const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

function generateSamplePDF() {
    const doc = new PDFDocument();
    const filePath = path.join(__dirname, '../tests/ecg-data/MRN-2024-001-ECG-Report.pdf');
    
    // Ensure drop-folder exists
    const dropFolder = path.join(__dirname, '../tests/ecg-data');
    if (!fs.existsSync(dropFolder)) {
        fs.mkdirSync(dropFolder, { recursive: true });
    }
    
    doc.pipe(fs.createWriteStream(filePath));
    
    // Header
    doc.fontSize(20)
        .text('OFFIN Healthcare - ECG Report', { align: 'center' })
        .moveDown();
    
    // Patient Information
    doc.fontSize(14)
        .text('Patient Information', { underline: true })
        .moveDown(0.5);
    
    doc.fontSize(12)
        .text(`Name: John Doe`)
        .text(`MRN: MRN-2024-001`)
        .text(`DOB: 1975-06-15`)
        .text(`Age: ${new Date().getFullYear() - 1975} years`)
        .text(`Gender: Male`)
        .moveDown();
    
    // ECG Measurements
    doc.fontSize(14)
        .text('ECG Measurements', { underline: true })
        .moveDown(0.5);
    
    doc.fontSize(12)
        .text(`Heart Rate: 72 bpm`)
        .text(`PR Interval: 160 ms`)
        .text(`QRS Duration: 98 ms`)
        .text(`QT Interval: 400 ms`)
        .text(`QTc Interval: 420 ms`)
        .moveDown();
    
    // Interpretation
    doc.fontSize(14)
        .text('Interpretation', { underline: true })
        .moveDown(0.5);
    
    doc.fontSize(12)
        .text('Normal sinus rhythm')
        .text('No acute findings')
        .moveDown();
    
    // Footer
    doc.fontSize(10)
        .text('Generated by OFFIN Healthcare Gateway', { align: 'center' })
        .text(`Report Date: ${new Date().toLocaleString()}`, { align: 'center' });
    
    doc.end();
    
    console.log(`✅ Sample PDF created: ${filePath}`);
    return filePath;
}

// Run the generator
if (require.main === module) {
    generateSamplePDF();
}

module.exports = { generateSamplePDF };